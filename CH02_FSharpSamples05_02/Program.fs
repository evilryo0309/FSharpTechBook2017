// *******************************************************
// Tuple 運算子 / 運算式 應用程式
// *******************************************************
open System

// *******************************************************
// Tuple 運算子 / 運算式 宣告與定義
// *******************************************************
let (n1, n2) = (100, 200)
let (total1, total2) = (n1 + 1000, n2 + 99)
Console.WriteLine((n1, n2))
Console.WriteLine((total1, total2))

// *******************************************************
// Tuple 運算子 : 混合式 Tuple 運算子 宣告
// *******************************************************
let mutable (x, y, z) = (1099, "Visual F# 2017", false)
Console.WriteLine((x, y, z))

// *******************************************************
// Tuple 運算子 : Tuple 運算子元素獨立運算
// *******************************************************
x <- x + 88

// *******************************************************
// Tuple 運算子 : Tuple 運算子元素執行邏輯運算
// *******************************************************
z <- z || true
Console.WriteLine((x, y, z))
Console.WriteLine(Console.ReadLine())